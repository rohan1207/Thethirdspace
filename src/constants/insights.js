export function getInsightById(id) {
  return INSIGHTS.find((article) => article.id === id) ?? null;
}

export function getInsightPath(id) {
  return `/insights/${id}`;
}

export function getAdjacentInsights(id) {
  const index = INSIGHTS.findIndex((article) => article.id === id);
  if (index === -1) return { prev: null, next: null };
  return {
    prev: index > 0 ? INSIGHTS[index - 1] : null,
    next: index < INSIGHTS.length - 1 ? INSIGHTS[index + 1] : null,
  };
}

export function getRelatedInsights(id, limit = 3) {
  const current = getInsightById(id);
  if (!current) return [];
  return INSIGHTS.filter(
    (article) => article.id !== id && article.category === current.category
  ).slice(0, limit);
}
